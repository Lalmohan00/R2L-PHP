							<h2>Basic Operators :</h2>

                            <h4>Python supports various basic operators, including:</h4>

							<p><b>1. Arithmetic Operators: </b> used for performing mathematical calculations on numeric data types. Examples include + (addition), - (subtraction), * (multiplication), / (division), % (modulus), ** (exponentiation), and // (floor division).</p>

							<p><b>2. Comparison Operators:</b>used for comparing two values and returning a Boolean value (True or False). Examples include == (equal to), != (not equal to), > (greater than), < (less than), >= (greater than or equal to), and <= (less than or equal to).</p>

                            <p><b>3. Logical Operators:</b>used for combining multiple Boolean expressions and returning a Boolean value. Examples include and (logical AND), or (logical OR), and not (logical NOT).</p>

                            <p><b>4. Assignment Operators: </b>used for assigning a value to a variable. Examples include = (simple assignment), += (add and assign), -= (subtract and assign), *= (multiply and assign), /= (divide and assign), %= (modulus and assign), **= (exponentiate and assign), and //= (floor divide and assign).</p>

                            <p><b>5. Identity Operators:</b>used for comparing the memory location of two objects. Examples include is (returns True if the two objects have the same memory address) and is not (returns True if the two objects have different memory addresses).</p>

                            <p><b>6. Membership Operators:</b>used for testing whether a value is a member of a sequence. Examples include in (returns True if the value is present in the sequence) and not in (returns True if the value is not present in the sequence).</p>

                            <p>These basic operators are used extensively in Python programming for performing various operations and making decisions based on the comparison and logical operations.</p>

                            <h2>Setup:</h2>

                            <h4>To get started with Python programming, you need to download and install Python on your system. Here are the steps to install Python:</h4>

                            <p><b>1. </b>Go to the official Python website at https://www.python.org/downloads/ and download the appropriate Python version for your operating system.</p>

                            <p><b>2. </b>Once the download is complete, run the Python installer by double-clicking on the downloaded file.</p>

                            <p><b>3. </b>Follow the on-screen instructions to complete the installation. You can choose the default options or customize the installation based on your preferences. </p>

                            <p><b>4. </b>After installation, Python will be added to your system's path, allowing you to access it from the command line. </p>

                            <p><b>5. </b>You can now launch Python from the command line or use an Integrated Development Environment (IDE) such as PyCharm, Visual Studio Code, or IDLE to write and execute Python code. </p>

                            <p>That's it! You're now ready to start coding in Python using the basic operators and other features of the language.</p>

							
