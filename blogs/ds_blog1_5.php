							<h2>Quantitative Technique and Graphical Gechnique</h2>
							<p>Quantitative techniques and graphical techniques are two common methods used in data analysis.</p>

							<p>Quantitative techniques involve the use of mathematical and statistical tools to analyze and interpret data. These techniques are often used to measure and compare different variables in a data set, identify trends and patterns, and make predictions about future outcomes. Some common quantitative techniques include regression analysis, hypothesis testing, time-series analysis, and Monte Carlo simulations.</p>

							<p>Graphical techniques, on the other hand, involve the use of visual representations to explore and summarize data. These techniques are often used to identify patterns and relationships between variables, communicate findings to others, and support decision-making. Some common graphical techniques include scatter plots, histograms, box plots, heat maps, and network diagrams.</p>
 
							<p>Both quantitative and graphical techniques have their own strengths and weaknesses, and can be used together to gain a more comprehensive understanding of the data. For example, quantitative techniques can help identify statistically significant relationships between variables, while graphical techniques can help visualize these relationships and make them easier to understand.</p>

							<h3>Conclusion</h3>

